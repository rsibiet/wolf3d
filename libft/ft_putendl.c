#include "libft.h"

void	ft_putendl(char const *s)
{
	int	i;
	int	j;

	i = ft_strlen(s);
	j = 0;
	while (j < i)
	{
		ft_putchar(s[j]);
		j++;
	}
	ft_putchar('\n');
}
